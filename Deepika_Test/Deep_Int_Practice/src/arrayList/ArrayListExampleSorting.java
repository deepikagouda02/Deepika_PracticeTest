package arrayList;
import java.util.*;

public class ArrayListExampleSorting {

	public static void main(String[] args) {
		ArrayList<String> list=new ArrayList<String>();
		list.add("XCVVNVVZ");
		list.add("QWERTYUGVH");
		list.add("ASDFGHKL");
		list.add("POIIUYTR");
		System.out.println("List: " +list);
		list.add(0, "ZQWPAWEERTRY");
		System.out.println("List: " +list);
			
	}

}
